    <h1>Introduction to PHP</h1>
    <p><strong>PHP (Hypertext Preprocessor)</strong> is a powerful and widely-used open-source server-side scripting language. It is specially designed for web development and can be embedded within HTML.</p>

    <p>With PHP, you can create dynamic web pages, interact with databases like MySQL, manage sessions, send emails, and much more. It runs on various platforms (Windows, Linux, macOS) and supports many databases.</p>

    <h2>Why Use PHP?</h2>
    <ul>
        <li>Easy to learn and use</li>
        <li>Free and open-source</li>
        <li>Works smoothly with HTML and databases</li>
        <li>Widely supported by web servers and hosting services</li>
    </ul>

    <h2>Example:</h2>
    <pre>
        &lt;?php
            echo "Hello, World!";
        ?&gt;
    </pre>

    <p>This simple code prints <strong>Hello, World!</strong> on the web page using PHP.</p>
